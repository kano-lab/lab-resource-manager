//! 非同期タスク実行
//!
//! バックグラウンドタスクの管理と実行を提供します。
//!
//! ## 責務
//!
//! Slackの応答タイムアウト（3秒）を回避するため、時間のかかる処理を
//! バックグラウンドで実行し、後から結果をSlackに送信する機能を提供します。
//!
//! Slackとの通信では、インタラクションに対して3秒以内に応答する必要があります。
//! しかし、Google Calendar APIへの書き込みなど、時間のかかる処理が必要な場合があります。
//! このモジュールは、そのような処理をバックグラウンドタスクとして実行し、
//! 完了後にSlackに結果を通知する仕組みを提供します。
//!
//! ## モジュール
//!
//! - `background_task`: バックグラウンドタスクの実行とレスポンス送信

pub mod background_task;
