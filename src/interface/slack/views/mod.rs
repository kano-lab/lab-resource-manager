//! Slack UIコンポーネント
//!
//! モーダルとメッセージの定義を提供します。
//!
//! ## 責務
//!
//! Slack Block Kit を使用したUIコンポーネント（モーダル、メッセージ）の
//! 構造を定義します。このモジュールはビュー層に相当し、ユーザーに表示される
//! UIの構築を担当します。
//!
//! ## Slack Block Kit との対応
//!
//! Slackの Block Kit は、モーダルやメッセージを構築するためのUIフレームワークです。
//! このモジュールは Block Kit の構造（Blocks、Elements、Objects）を
//! Rustのコードで表現し、再利用可能なビルダー関数として提供します。
//!
//! ## モジュール
//!
//! - `messages`: メッセージブロック定義（確認メッセージ、エラーメッセージ）
//! - `modals`: モーダルビュー定義（登録、予約、更新のモーダル）

pub mod messages;
pub mod modals;
