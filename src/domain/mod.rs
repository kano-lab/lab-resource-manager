//! # Domain Layer
//!
//! このディレクトリはクリーンアーキテクチャのDomain層を表す。
//!
//! ## Domain層とは
//!
//! ビジネスロジックとドメインルールを表現する、アプリケーションの中核となる層。
//! 外部の技術的詳細に依存せず、エンティティ、値オブジェクト、ドメインサービスなどを含む。
//!
//! ## 依存のルール
//!
//! Domain層は他のどの層にも依存しない。
//! 外部ライブラリへの依存も最小限に抑える。
